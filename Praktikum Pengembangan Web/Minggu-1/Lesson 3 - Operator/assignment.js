let c = 20 ;

c += 10;
console.log("c setelah ditambah 10:", c); // [ 'c setelah ditambah 10:', 30 ]

c -= 5;
console.log("c setelah dikurangi 5:", c); // [ 'c setelah dikurangi 5:', 25 ]

c *= 2;
console.log("c setelah dikali 2:", c); // [ 'c setelah dikali 2:', 50 ]

c /= 5;
console.log("c setelah dibagi 5:", c); // [ 'c setelah dibagi 5:', 10 ]

c %= 3;
console.log("c setelah sisa bagi 3:", c); // [ 'c setelah sisa bagi 3:', 1 ]