let notDefined;
console.log("Undefined:", notDefined); // [ 'Undefined:', undefined ]

let nullValue = null;
console.log("Null:", nullValue); // [ 'Null:', null ]

let age = 25;
console.log("Number:", age); // [ 'Number:', 25 ]

let nama = "John Doe";
console.log("String:", nama); // [ 'String:', 'John Doe' ]

let isStudent = true;
console.log("Boolean:", isStudent); // [ 'Boolean:', true ]

let person = {
    firstName: "Jane",
    lastName: "Doe",
    age: 30
};
console.log("Object:", person); // [ 'Object:', { firstName: 'Jane', lastName: 'Doe', age: 30 } ]

let fruits = ["Apple", "Banana", "Cherry"];
console.log("Array:", fruits); // [ 'Array:', [ 'Apple', 'Banana', 'Cherry' ] ]

let currentDate = new Date();
console.log("Date:", currentDate); // [ 'Date:', new Date('2024-09-03T16:29:56.000Z') ]