let x = 8;
let y = 10;

console.log(x == y);  // false, karena 8 tidak sama dengan 10
console.log(x != y);  // true, karena 8 tidak sama dengan 10
console.log(x > y);   // false, karena 8 tidak lebih besar dari 10
console.log(x < y);   // true, karena 8 lebih kecil dari 10
console.log(x >= y);  // false, karena 8 tidak lebih besar atau sama dengan 10
console.log(x <= y);  // true, karena 8 lebih kecil atau sama dengan 10